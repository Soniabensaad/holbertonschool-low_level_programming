#include "main.h"

/**
 * main - prints name, followed by a new line
 *
 * @argc : integer
 * @argv : array of strings
 *
 * Description: a program that prints name, followed by a new line.
 *
 * Return: Always 0
 */
int main(int argc, char *argv[])
{
	if (argc > 0)
	{
		argv = argv;
		printf("%d\n", argc - 1);
	}
	return (0);
}